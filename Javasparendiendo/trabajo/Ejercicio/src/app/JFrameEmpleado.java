    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tab.getModel();
        model.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsm;
        int co;
        try {
            Connection cn = Conexion.getConexion();
            ps = cn.prepareStatement("SELECT id,Nombre,Apellido,Edad,Puesto,NivelAcademico,Profesion,Dni FROM Alumnos");
            rs = ps.executeQuery();
            rsm = rs.getMetaData();
            co = rsm.getColumnCount();

            while (rs.next()) {
                Object[] fi = new Object[co];
                for (int indice = 0; indice < co; indice++) {
                    fi[indice] = rs.getObject(indice + 1);

                }
                model.addRow(fi);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        int id = Integer.parseInt(txtid.getText());
        String Nombre = txtnombre.getText();
        String Apellido = txtapellido.getText();
        int Edad = Integer.parseInt(txtedad.getText());
        String Puesto = txtpuesto.getText();
        String na = txtacademico.getText();
        String Profesion = txtprofesion.getText();
        int dni = Integer.parseInt(txtdni.getText());

        try {
            Connection cn = Conexion.getConexion();
            PreparedStatement ps = cn.prepareStatement("INSERT INTO Alumnos(id,Nombre,Apellido,Edad,Puesto,NivelAcademico,Profesion,Dni) VALUES(?,?,?,?,?,?,?,?)");
            ps.setInt(1, id);
            ps.setString(2, Nombre);
            ps.setString(3, Apellido);
            ps.setInt(4, Edad);
            ps.setString(5, Puesto);
            ps.setString(6, na);
            ps.setString(7, Profesion);
            ps.setInt(8, dni);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Guardado");
            clean();
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_btnagregarActionPerformed

