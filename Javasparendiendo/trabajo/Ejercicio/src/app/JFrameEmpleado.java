    private void tabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabMouseClicked
        try {
            int row = tab.getSelectedRow();
            int id = Integer.parseInt(tab.getValueAt(row, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            Connection cn = Conexion.getConexion();
            ps = cn.prepareStatement("SELECT Nombre,Apellido,Edad,Puesto,NivelAcademico,Profesion,Dni FROM Alumnos WHERE id=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();

            while (rs.next()) {
                txtid.setText(String.valueOf(id));
                txtnombre.setText(rs.getString("Nombre"));
                txtapellido.setText(rs.getString("Apellido"));
                txtedad.setText(String.valueOf(rs.getInt("Edad")));
                txtpuesto.setText((rs.getString("Puesto")));
                txtacademico.setText(rs.getString("NivelAcademico"));
                txtprofesion.setText(rs.getString("Profesion"));
                txtdni.setText(String.valueOf(rs.getInt("Dni")));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        txtid.setEnabled(false);
    }//GEN-LAST:event_tabMouseClicked

